#!/usr/bin/env node
/**
 * Regenerates src/data/selected-works-blur.ts after changing files in
 * public/images/selected-works/. Tiny WebP placeholders (~0.5 KB each) for
 * Next.js Image blur placeholders (better perceived load than a gray SVG).
 */
import { promises as fs } from 'node:fs';
import path from 'node:path';
import sharp from 'sharp';

const ROOT = path.resolve(process.cwd(), 'public/images/selected-works');
const OUT = path.resolve(process.cwd(), 'src/data/selected-works-blur.ts');

const files = [
  'sw-01-estate-infinity-twilight.webp',
  'sw-02-pavilion-kitchen-sunset.webp',
  'sw-03-culinary-terrace.webp',
  'sw-04-pool-fire-evening.webp',
  'sw-05-sculptural-pool-garden.webp',
  'sw-06-arrival-court.webp',
];

async function main() {
  const map = {};
  for (const f of files) {
    const full = path.join(ROOT, f);
    const buf = await sharp(full).resize(40, 25, { fit: 'cover' }).webp({ quality: 50 }).toBuffer();
    map['/images/selected-works/' + f] = `data:image/webp;base64,${buf.toString('base64')}`;
  }

  const src = `/** Auto-generated by scripts/generate-selected-works-blur.mjs, run after changing selected-works images */
export const selectedWorksBlurBySrc: Record<string, string> = ${JSON.stringify(map, null, 2)};
`;

  await fs.writeFile(OUT, src, 'utf8');
  console.log(`Wrote ${Object.keys(map).length} blur placeholders to ${path.relative(process.cwd(), OUT)}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
